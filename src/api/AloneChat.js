import request from "@/utils/request.js";


export const getAloneChatListService = () =>
    request.post('/api/aloneChat/getAloneChatList')

export const getFriendDataService = (friendId) =>
    request.post('/api/aloneChat/getFriendData', { friendId})

export const getFriendDataListService = () =>
    request.post('/api/aloneChat/getFriendDataList')

export const getChatHistoryService = (friendId) =>
    request.post('/api/aloneChat/getChatHistory', { friendId})

export const sendMessageToFriendService = (friendId, content) =>
    request.post('/api/aloneChat/sendMessage', { friendId, content})

export const updateFriendInfoService = ( userId, remark , groupId ,isEmailNotify) =>
    request.post('/api/aloneChat/updateFriendInfo', {userId, remark, groupId, isEmailNotify})

